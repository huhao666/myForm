import * as echarts from '../../component/ec-canvas/echarts.js';

const app = getApp()

function initChart(canvas, width, height, dpr) {
  const chart = echarts.init(canvas, null, {
    width: width,
    height: height,
    devicePixelRatio: dpr // 像素
  });
  canvas.setChart(chart);

  var option = {
    title: {
      text: app.graph.title,
      left: 'center'
    },
    tooltip: {
      trigger: 'item',
      formatter: '{c}个({d}%)'
    },
    legend: {
      top: 40,
      data: app.graph.legend
    },
    series: [{
        name: '访问来源',
        type: 'pie',
        radius: '50%',
        // roseType: "area",
        data: app.graph.list,
        emphasis: {
          itemStyle: {
            shadowBlur: 10,
            shadowOffsetX: 0,
            shadowColor: 'rgba(0, 0, 0, 0.5)'
          }
        }
      },

    ]
  };

  chart.setOption(option);
  return chart;
}

